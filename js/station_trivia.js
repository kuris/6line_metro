/* 빠른 검색용 함수 (STATION_TRIVIA 맵 활용) */
function getTrivia(stationId) {
  const sets = STATION_TRIVIA[stationId] || STATION_TRIVIA['default'];
  const idx = Math.floor(Math.random() * sets.length);
  return sets[idx];
}
