const stationListKeys = Object.keys(stationOrigin);

async function maybeRunHanjaQuiz(st) {
  const freq = G.hanjaQuizFreq ?? 1.0;
  if (freq <= 0) return false;
  
  // 이미 정답을 맞춘 역은 퀴즈를 건너뜀 (중복 방지)
  if (G.quizSolved && G.quizSolved.includes(st.name)) return false;

  if (Math.random() > freq) return false;

  const data = stationOrigin[st.name];
  if (!data) return false;

  TrainPanel.addLog(`[승객 식별 제어] 고대 지명 해독 가동`, 'warn');
  
  let options = [{ text: `${data.meaning}`, correct: true }];
  
  if (data.fakes) {
    data.fakes.forEach(f => options.push({ text: f, correct: false }));
  } else {
    const others = stationListKeys.filter(k => k !== st.name).sort(() => 0.5 - Math.random());
    options.push({ text: stationOrigin[others[0]].meaning, correct: false });
    options.push({ text: stationOrigin[others[1]].meaning, correct: false });
  }

  options.sort(() => 0.5 - Math.random());
  options.push({ text: "단념한다 (아무것도 떠올리고 싶지 않다)", correct: false, isGiveUp: true });

  await seq([
    ['', 'blank', 0],
    [`"...다음 역은 ${st.name}, ${st.name}역입니다."`, 'announce', 800],
    [`${st.name}... ${data.topic}. 지명의 본질이 뇌리를 스칩니다.`, 'narrator', 1500],
    ['이름이 가진 힘을 정확히 통제해야만 눈앞의 주술적 함정에서 벗어날 수 있습니다.', 'system', 2000],
  ]);

  return new Promise(resolve => {
    const choiceOpts = options.map((opt, idx) => [
      `${idx + 1}) [${opt.text}]`,
      async () => {
        if (opt.correct) {
          G.hanjaAttempts = (G.hanjaAttempts || 0) + 1;
          G.hanjaSuccess = (G.hanjaSuccess || 0) + 1;
          
          // 정답 기록 및 힌트 아이템 지급
          if (!G.quizSolved) G.quizSolved = [];
          if (!G.quizSolved.includes(st.name)) G.quizSolved.push(st.name);
          addItem(`💡 힌트: ${st.name}`);

          G.sanity = Math.min(100, G.sanity + data.sanityBonus);
          G.score += 5;
          updateStats();
          if (window.sfx && window.sfx.item) window.sfx.item();
          TrainPanel.addLog(`[해독 성공] '💡 힌트: ${st.name}' 획득`, 'life');
          
          let correctSeq = [
            [`맞아, ${data.meaning}.`, 'highlight', 1200]
          ];
          data.storyCorrect.forEach(line => {
             correctSeq.push([line, 'life', 2200]);
          });
          correctSeq.push([`魂(혼) +${data.sanityBonus} — 본질을 꿰뚫어 진정한 이성을 회복합니다.`, 'system', 1500]);
          await seq(correctSeq);
          
          resolve(true);
        } else if (opt.isGiveUp) {
          G.hanjaFail = (G.hanjaFail || 0) + 1;
          G.sanity = Math.max(0, G.sanity - 3);
          updateStats();
          if (window.sfx && window.sfx.boom) window.sfx.boom(0.1);
          TrainPanel.addLog('[해독 단념] 스스로 눈과 귀를 도려냈습니다', 'warn');
          
          await seq([
            ['단념했습니다. 모르는 채로 휩쓸리는 쪽이 어쩌면 편안할지도 모릅니다.', 'narrator', 1500],
            ['시각과 청각이 마비되며 보이지 않는 촉수 같은 올가미들이 전신을 결박해 옵니다.', 'death', 2500],
            ['魂(혼) -3 — 의지와 주도권의 비참한 감각 상실.', 'warn', 1500],
          ]);
          resolve(true);
        } else {
          G.hanjaFail = (G.hanjaFail || 0) + 1;
          G.sanity = Math.max(0, G.sanity - (data.sanityBonus + 2));
          updateStats();
          if (window.HorrorFX) window.HorrorFX.flashRed(600);
          if (window.sfx && window.sfx.boom) window.sfx.boom(0.2);
          TrainPanel.addLog('[치명적 오류] 오독으로 인한 인지 체계 및 신체 붕괴', 'danger');
          
          let failSeq = [
            [`틀렸어... 당신이 떠올린 의미는 망령들이 유도한 죽음의 은어일 뿐.`, 'death', 1500]
          ];
          data.storyFail.forEach(line => {
             failSeq.push([line, 'danger', 2500]);
          });
          failSeq.push([`魂(혼) -${data.sanityBonus + 2} — 어설픈 지식이 칼날이 되어 등줄기를 갈라버립니다.`, 'warn', 1500]);
          await seq(failSeq);

          resolve(true);
        }
      }
    ]);
    
    choices(choiceOpts);
  });
}

window.maybeRunHanjaQuiz = maybeRunHanjaQuiz;
